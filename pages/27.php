<h2>PAC 27+</h2>
<div class="well">
	<p>
		PAC 27+ is our sub-group that shows anime which otherwise would not fit in the regular lineup due to the length
		of the show. It usually features shows which are greater than 27 episodes in length. The meetings are generally
		held in Grissom and announced on a weekly basis for Wednesday nights.
	</p>
