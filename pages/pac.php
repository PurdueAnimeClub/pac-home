<h2>Purdue Anime Club</h2>
<div class="well">
	<p>
		Purdue Anime Club (PAC) is Purdue University's premier anime viewing club. Purdue Anime Club meets once a week
		on Thursdays from 7 p.m. to around 11:30 p.m. and is typically held in Stewart Center: located nearly smack dab
		in the middle of campus. Generally meetings start between 7 and 7:30 and have a 15-30 minute break after the
		first four shows. Rooms and locations can change on a weekly basis, so be sure to keep tabs by checking the
		annoucements often. 
	</p>
</div>
